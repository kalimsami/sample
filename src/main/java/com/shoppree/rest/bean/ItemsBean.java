package com.shoppree.rest.bean;

public class ItemsBean {

	private String product_id;
	private String no_of_purchases;
	private String red_product_id;
	
	
	public String getProduct_id() {
		return product_id;
	}
	public void setProduct_id(String product_id) {
		this.product_id = product_id;
	}
	public String getNo_of_purchases() {
		return no_of_purchases;
	}
	public void setNo_of_purchases(String no_of_purchases) {
		this.no_of_purchases = no_of_purchases;
	}
	public String getRed_product_id() {
		return red_product_id;
	}
	public void setRed_product_id(String red_product_id) {
		this.red_product_id = red_product_id;
	}
}
